public final class ForLoop {
  public static void main(String[] args) {
    for (int i = 0; i < 5; i++) {
      System.out.println(i);
    }
    for (int i = 0; i <= 5; i++) {
      System.out.println(i);
    }
    for (int i = 5; i > 0; i--) {
      System.out.println(i);
    }
    for (int i = 5; i >= 0; i--) {
      System.out.println(i);
    }
  }
}